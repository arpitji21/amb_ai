@app.get("/api/family/{ambulance_id}")
def get_family_view(ambulance_id: str):
    """Everything the Family dashboard needs in one call: condition, ETA,
    hospital, live location, and a plain-language message."""
    for a in fleet.ambulances:
        if a.id == ambulance_id:
            timeline = ["Ambulance dispatched", "Patient picked up", "Doctor reviewing", "Hospital informed"]
            if a.status == "arrived":
                timeline.append("Arrived at hospital")
            elif a.eta_minutes is not None:
                timeline.append(f"Arriving in {a.eta_minutes} minutes")
            return {
                "ambulance_id": a.id,
                "patient_name": a.patient.name if a.patient else None,
                "condition": "Critical — being closely monitored" if a.status == "critical" else "Stable",
                "eta_minutes": a.eta_minutes,
                "hospital": a.hospital,
                "lat": a.lat,
                "lng": a.lng,
                "status": a.status,
                "message": sim.family_message(a),
                "timeline": timeline,
            }
    return {"error": "not found"}, 404


@app.post("/api/ambulances/{ambulance_id}/notify-hospital")
def notify_hospital(ambulance_id: str, db: Session = Depends(get_db)):
    for a in fleet.ambulances:
        if a.id == ambulance_id:
            message = f"{a.hospital} notified — ETA {a.eta_minutes or '—'} min, status {a.status}."
            db.add(models.NotificationRecord(ambulance_id=a.id, hospital=a.hospital, message=message))
            log_event(db, f"{a.id} — hospital notified ({a.hospital})", ambulance_id=a.id)
            return {"ok": True, "message": message}
    return {"error": "not found"}, 404


@app.post("/api/ambulances/{ambulance_id}/send-to-hospital")
def send_to_hospital(ambulance_id: str, db: Session = Depends(get_db)):
    for a in fleet.ambulances:
        if a.id == ambulance_id:
            if a.status == "available":
                return {"error": "Unit has no active patient to transport"}, 400
            a.status = "responding" if a.status != "critical" else "critical"
            persist_fleet_snapshot(db)
            log_event(db, f"{a.id} sent to {a.hospital}", ambulance_id=a.id)
            return {"ok": True, "status": a.status, "hospital": a.hospital}
    return {"error": "not found"}, 404


@app.websocket("/ws/ambulances")
async def ws_ambulances(websocket: WebSocket):
    """
    Streams the full fleet state every 2 seconds (matching the "GPS updates
    every two seconds" requirement) plus discrete event notifications
    whenever a dispatch, arrival, or critical alert occurs. Every tick's
    result and every event is also persisted to the database.
    """
    await websocket.accept()
    try:
        await websocket.send_text(json.dumps({"type": "ambulances", "data": fleet.to_dict()}))
        while True:
            await asyncio.sleep(2)
            events = fleet.tick()
            db = SessionLocal()
            try:
                persist_fleet_snapshot(db)
                for text in events:
                    ambulance_id = text.split(" ", 1)[0]
                    log_event(db, text, ambulance_id=ambulance_id)
            finally:
                db.close()
            await websocket.send_text(json.dumps({"type": "ambulances", "data": fleet.to_dict()}))
            for text in events:
                await websocket.send_text(json.dumps({"type": "event", "text": text}))
    except WebSocketDisconnect:
        pass
