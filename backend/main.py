"""
LEIP backend — MVP demo API.

Serves ambulance, hospital, doctor, and dashboard-statistics data over REST,
and streams live ambulance movement/vitals over a WebSocket. No real medical
devices are connected; the live feed is generated by `simulation.py`.

Every tick's result — and every dispatch/arrival/notification event — is
persisted to the database (SQLite by default, Postgres via DATABASE_URL), so
the fleet, activity log, and notifications all survive a backend restart.

Run:
    pip install -r requirements.txt
    uvicorn main:app --reload
"""
