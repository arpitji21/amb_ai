def _ambulance_row_to_dict(row: models.AmbulanceRecord) -> dict:
    return {
        "id": row.id, "driver": row.driver, "driver_phone": row.driver_phone, "status": row.status,
        "lat": row.lat, "lng": row.lng, "speed": row.speed,
        "eta_minutes": row.eta_minutes, "hospital": row.hospital,
        "patient_name": row.patient_name, "patient_age": row.patient_age,
        "patient_gender": row.patient_gender, "patient_blood_group": row.patient_blood_group,
        "patient_history": row.patient_history, "patient_allergies": row.patient_allergies,
        "heart_rate": row.heart_rate, "bp_systolic": row.bp_systolic, "bp_diastolic": row.bp_diastolic,
        "spo2": row.spo2, "temperature": row.temperature, "respiratory_rate": row.respiratory_rate,
    }


def persist_fleet_snapshot(db: Session):
    for row in fleet.snapshot_rows():
        db.merge(models.AmbulanceRecord(**row))
    db.commit()


def log_event(db: Session, text: str, ambulance_id: str | None = None):
    db.add(models.EventRecord(ambulance_id=ambulance_id, text=text))
    db.commit()


@app.on_event("startup")
def startup_seed_and_hydrate():
    """Runs once at boot: seeds demo users/hospitals/doctors on first run,
    and resumes the ambulance fleet from the database if it was persisted
    by a previous run — otherwise seeds a fresh fleet and saves it."""
    global fleet
    db = SessionLocal()
    try:
        for u in DEMO_USERS:
            if not db.query(models.User).filter(models.User.email == u["email"]).first():
                db.add(models.User(
                    name=u["name"], email=u["email"], role=u["role"],
                    hashed_password=hash_password(u["password"]), hospital=u.get("hospital"),
                ))

        if not db.query(models.HospitalRecord).first():
            for h in sim.HOSPITALS:
                db.add(models.HospitalRecord(name=h["name"], lat=h["lat"], lng=h["lng"], load=h["load"]))

        if not db.query(models.DoctorRecord).first():
            for d in sim.DOCTORS:
                db.add(models.DoctorRecord(name=d["name"], specialty=d["specialty"], hospital=d["hospital"]))

        db.commit()

        rows = db.query(models.AmbulanceRecord).all()
        if rows:
            fleet = sim.Fleet(seed_rows=[_ambulance_row_to_dict(r) for r in rows])
            print(f"Resumed fleet from database ({len(rows)} units).")
        else:
            fleet = sim.Fleet()
            persist_fleet_snapshot(db)
            log_event(db, "Command center initialized — fleet seeded")
            print("Seeded a fresh fleet into the database.")
    finally:
        db.close()

    print("Demo logins ready — password 'demo1234' for: " + ", ".join(u["email"] for u in DEMO_USERS))


